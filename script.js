function openWindow(){
    let top = window.pageYOffset;
    top = top + 100;
    document.getElementById('window1').style.display = 'block';
    document.getElementById('window1').style.top = top +'px';
  }
  function closeWindow(){
    let top = window.pageYOffset;
    top = top + 100;
    document.getElementById('window1').style.display = 'none';
    document.getElementById('window1').style.top = top +'px';
  }
